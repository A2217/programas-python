# Acceder a los elementos de una lista

n = [10, 20, 30, 40, 60, 70, 10, 20, 99]

print("Acceder a los elementos de una lista\n")

print(f"Cuantos números son : {len(n)} \n")
print(f"Todos los números : {n} \n")
print(f"Primero y último : {n[0]}, {n[-1]} \n")
print(f"Del 2 al 6 : {n[2:6]} \n")
print(f"Los primeros 3 : {n[:3]} \n")
print(f"Los últimos 3 : {n[6:]} \n")